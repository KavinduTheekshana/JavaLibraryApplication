        public void displayReturnBookDetails(String MembershipNo){
        ResultSet rs = new DbSearch().searchBorrowReturn(MembershipNo);
        DefaultTableModel dtm = (DefaultTableModel)
                tblreturnBook.getModel();
                dtm.setRowCount(0);
        try
        {
            Vector v;
            while(rs.next())
            {
                v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("book_id"));
                v.add(rs.getString("bookname"));
                v.add(rs.getString("category"));
                v.add(rs.getString("borrowing_date"));
                v.add(rs.getString("returning_date"));
                dtm.addRow(v);
            }
            DbConnection.closeCon();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
     }
        

        


